class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        vector<long long> dp(n+2);
        for(int i=(n-1);i>=0;i--){
            long long x = dp[i+1];
            long long y = dp[i+2] + arr[i];
            dp[i] = max(x,y);
        }
        return dp[0];
    }
};

